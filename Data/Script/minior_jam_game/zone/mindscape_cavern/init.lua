    -- do nothing???
  elseif result == RogueEssence.Data.GameProgress.ResultType.Failed then
    UI:SetSpeaker(GAME:GetPlayerPartyMember(1))
    UI:SetSpeakerEmotion("Pain")
    UI:WaitShowDialogue("Urk...[pause=20] This is harder than I thought...[pause=20] Let's head home for now...")
    COMMON.EndDungeonDay(result, 'mellow_town', -1, 0, 1)
  else
    --COMMON.EndDungeonDay(result, 'mellow_town', -1, 0, 1)
    if segmentID == 0 then --exiting from first half of dungeon
      GAME:EnterZone("mindscape_cavern", -1, 0, 0)
    elseif segmentID == 1 then --Exiting from second half of dungeon
      if SV.mindscape_cavern.BossDefeated ~= true then
        GAME:EnterZone("mindscape_cavern", -1, 1, 0) -- Send to ground map for cutscene
      else
        GAME:EndDungeonDay(result, "mellow_town", -1, 0, 1)
      end
    elseif segmentID == 2 then --exiting from boss room
      SV.mindscape_cavern.BossDefeated = true
      GAME:EnterZone("mindscape_cavern", -1, 1, 0) -- Send to ground map for cutscene
    end
  end
